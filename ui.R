library(fresh)

sidebar_size = '300px'

header = dashboardHeader(
  title='Dashboard Covid SP',
  titleWidth = sidebar_size
)

sidebar = dashboardSidebar(
  tags$link(
    rel = 'stylesheet',
    type = 'text/css',
    href = 'main.css'
  ),
  width = sidebar_size,
  
  fluidRow(
    column(
      width=12,
      selectizeInput(
        'selectize_municipio', 
        label = 'Município:', 
        choices = unique(data$municipio),
        options = list(maxItems = 10),
        selected = c('Ribeirão Preto', 'Barretos', 'Campinas'),
        multiple = TRUE
      )
    )
  ),
  
  fluidRow(
    column(
      width=6,
      dateInput('inicial_date',
                label = 'Data inicial:',
                format = 'dd/mm/yyyy',
                value = min(data$data),
                min = min(data$data),
                max = max(data$data)
                )
    ),
    column(
      width=6,
      dateInput('final_date',
                label = 'Data final:',
                format = 'dd/mm/yyyy',
                value = max(data$data),
                min = min(data$data),
                max = max(data$data)
                )
    )
  ),
  
  fluidRow(
    column(
      width=12,
      actionButton(
        'button_submit', 
        label = 'Consultar', 
        class='btn-primary btn-large', 
        style='color: #fff; width: 90%;')
    )
  )
  
)

body = dashboardBody(
  tags$link(
    rel = 'stylesheet',
    type = 'text/css',
    href = 'main.css'
  ),
  fluidRow(
    column(
      width = 6,
      box(
        class = 'chart-selection',
        width = '100%',
        actionButton('action', label='Evolução dos casos por município', class='btn btn-primary'),
      )
    ),
  ),
  fluidRow(
    column(
      width = 6,
      box(
        class = 'box-evolucao-casos',
        width = '100%',
        plotlyOutput('chart_evolucao_casos')
      )
    ),
    column(
      width = 6,
      box(
        width = '100%',
        plotlyOutput('chart_evolucao_obitos')
      )
    ),
    column(
      width = 12,
      box(
        class = 'box-obitos-por-habitante',
        width = '100%',
        plotlyOutput('chart_obitos_por_habitante')
      )
    ),
    column(
      width = 12,
      box(
        width = '100%',
        plotlyOutput('chart_evol_obitos_por_caso')
      )
    )
  ),
  
  tags$script(
    src = 'main.js'
  )
)

dashboardPage(
  header = header, 
  sidebar = sidebar, 
  body = body
)